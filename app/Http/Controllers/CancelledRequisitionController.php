    public function show(Requisition $cancelrequisition)
    {
        $items = Item::all();
        $requisitionItems = $cancelrequisition->items->pluck('pivot');

        return view('cancelrequisitions.show', [
            'requisition' => $cancelrequisition,
            'items' => $items,
            'requisitionItems' => $requisitionItems,
        ]);
    }

    public function edit(Requisition $cancelrequisition)
    {
        $items = Item::all();
        $requisitionItems = $cancelrequisition->items->pluck('pivot');

        return view('cancelrequisitions.edit', [
            'requisition' => $cancelrequisition,
            'items' => $items,
            'requisitionItems' => $requisitionItems,
        ]);
    }


    public function update(UpdateRequisitionRequest $request, Requisition $cancelrequisition)
    {
        $cancelrequisition->update([
            'branch_code'=> $request->branch_code,
            'req_no' => $request->req_no,
            'req_date' => $request->req_date,
            'status' => $request->status,
        ]);
        
        $cancelrequisition->items()->detach();

        foreach($request->items as $item) {
            $cancelrequisition->items()->attach($item['item_id'], ['quantity' => $item['quantity']]);
        }
        
        return redirect()->route('cancelrequisitions.index')->with('success', 'Requisition created successfully');
    }
