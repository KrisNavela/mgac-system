    public function update(UpdateRequisitionRequest $request, Requisition $fortransmittal)
    {
        $fortransmittal->update([
            //'branch_code'=> $request->branch_code,
            //'req_no' => $request->req_no,
            //'req_date' => $request->req_date,
            'status' => 'done',
            //'bonds_status' => $request->bonds_status,
            //'uw_status' => $request->uw_status,
            //'type_request' => $request->type_request,
            //'replenishment_month' => $request->replenishment_month,
            //'replenishment_year' => $request->replenishment_year,
        ]);
        
        $fortransmittal->items()->detach();

        foreach($request->items as $item) {

            if ($item['quantity_unit'] === 'Pad'){
                $fortransmittal->items()->attach($item['item_id'], [
                    'quantity' => $item['quantity'], 
                    'quantity_unit' => $item['quantity_unit'],
                    'in_pcs' => $item['quantity'] * 50,
                    'ho_ctrl_start' => $item['ho_ctrl_start'],
                    'ho_ctrl_end' => $item['ho_ctrl_end'],
                    'coc_prefix' => $item['coc_prefix'],
                    'series_start' => $item['series_start'],
                    'series_end' => $item['series_end']
                ]);
            } else {
                $fortransmittal->items()->attach($item['item_id'], [
                    'quantity' => $item['quantity'], 
                    'quantity_unit' => $item['quantity_unit'],
                    'in_pcs' => $item['quantity'],
                    'ho_ctrl_start' => $item['ho_ctrl_start'],
                    'ho_ctrl_end' => $item['ho_ctrl_end'],
                    'coc_prefix' => $item['coc_prefix'],
                    'series_start' => $item['series_start'],
                    'series_end' => $item['series_end']
                ]);
            }

            //Get specific item_code in parent-child relationship
            $id = $item['item_id'];
            $lineitem = Item::find($id);
            
            // Get the start and end values
            $start = $item['series_start'];
            $end = $item['series_end'];

            // Store the series in the database
            for ($i = $start; $i <= $end; $i++) {
                NumberSeries::create([
                    'number' => $i,
                    'requisition_id' => $fortransmittal->id,
                    'branch_code' => $request->branch_code,
                    'branch_name' => $request->branch_name,
                    //'item_code' => $itemid,
                    'item_id' => $item['item_id'],
                    'item_code' => $lineitem->item_code,
                    'number_status' => "Unused",
                    'coc_prefix' => $request->coc_prefix,
                ]);
            }
        }

        $requisitionid = $fortransmittal->id;
        $branches = branch::all();
        $users = User::all();

        $attachments = RequisitionAttachment::where('requisition_id',$requisitionid)->get();
        $remarks = RequisitionRemarks::where('requisition_id',$requisitionid)->get();
        $items = Item::all();
        $requisitionItems = $fortransmittal->items->pluck('pivot');

        return view('fortransmittal.edit', [
            'requisition' => $fortransmittal,
            'branches'=> $branches,
            'users'=> $users,
            'items' => $items,
            'requisitionItems' => $requisitionItems,
            'remarks' => $remarks,
            'attachments' => $attachments,
        ]);
        
        //return redirect()->route('fortransmittal.index')->with('success', 'Requisition created successfully');
    }

