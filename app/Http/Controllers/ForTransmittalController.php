    public function printPDF($id)
    {
        // Retrieve the record data
        $fortransmittal = Requisition::findOrFail($id);
        $items = Item::all();
        $requisitionItems = $fortransmittal->items->pluck('pivot');

        // Load the view and pass the data
        $pdf = Pdf::loadView('fortransmittal.pdf',[
            'requisition' => $fortransmittal,
            'items' => $items,
            'requisitionItems' => $requisitionItems,
        ]);

        // Stream the PDF in the browser or download it
        return $pdf->stream('fortransmittal.pdf',[
            'requisition' => $fortransmittal,
            'items' => $items,
            'requisitionItems' => $requisitionItems,
        ]);
    }
